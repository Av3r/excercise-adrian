package object_programming_2.snow.ui.model;

public class Dimension {

    private final int width;
    private final int height;

    public Dimension(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeignt() {
        return height;
    }
}
