package object_programming_2.snow.ui.model;

public class Dimension {

    private final int width;
    private final int heignt;

    public Dimension(int width, int heignt) {
        this.width = width;
        this.heignt = heignt;
    }

    public int getWidth() {
        return width;
    }

    public int getHeignt() {
        return heignt;
    }
}
